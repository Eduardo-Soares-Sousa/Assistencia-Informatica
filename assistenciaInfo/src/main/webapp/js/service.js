window.onload = function() {
    const formBox = document.querySelector('.form-box');
    formBox.style.opacity = 0;
    formBox.style.transition = 'opacity 0.5s ease-in-out';

    setTimeout(function() {
        formBox.style.opacity = 1;
    }, 2000);
};

